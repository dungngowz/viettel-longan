function openCameraDetails(index) {
  const camera = cameras[index];

  $("#cameraDetailModal").modal("show");

  const slidesHTML = camera.img
    .map(
      (imgSrc) => `
        <div class="swiper-slide">
            <img src="${imgSrc}" width="100%" />
        </div>
    `
    )
    .join("");

  const contentSlider = document.getElementById("camera-swiper-slider");
  const contentDetail = document.getElementById("camera-swiper-detail");

  contentSlider.innerHTML = `
  <div>
                        <div class="swiper camera-swiper">
                            <div class="swiper-wrapper">
                                ${slidesHTML}
                            </div>

                            <div class="swiper-button-next"></div>
                            <div class="swiper-button-prev"></div>
                        </div>

                        <div thumbsSlider="" class="swiper camera-swiper-thumbs">
                            <div class="swiper-wrapper">
                                                               ${slidesHTML}
                            </div>
                        </div>
                    </div>
  `;

  contentDetail.innerHTML = `
  <div>
  
  <div class="fs-4 fw-bold mt-3 mt-md-0">${camera.name}</div>
  <div>
  <span>Giá:</span>
  <span class="fs-6 fw-bold text-danger">${camera.discountPrice} </span>
  </div>

                        <div class="fw-bold fs-5 mt-3 pb-0">Đặc điểm nổi bật</div>

  ${camera.descDetails}</div>
  `;

  // Initialize Swiper instances
  var swiperThumbs = new Swiper(".camera-swiper-thumbs", {
    loop: true,
    spaceBetween: 10,
    slidesPerView: 6,
    freeMode: true,
    watchSlidesProgress: true,
  });

  var cameraSwiper = new Swiper(".camera-swiper", {
    loop: true,
    spaceBetween: 10,
    navigation: {
      nextEl: ".swiper-button-next",
      prevEl: ".swiper-button-prev",
    },
    thumbs: {
      swiper: swiperThumbs,
    },
  });
}

